package com.nokia.example.voip.model;

public class Settings {
    private String username;
    private String password;
    private String registrar;
    private String ringtoneUrl;
    private boolean showCallerId = true;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRegistrar() {
        return registrar;
    }

    public void setRegistrar(String registrar) {
        this.registrar = registrar;
    }

    public String getRingtoneUrl() {
        return ringtoneUrl;
    }

    public void setRingtoneUrl(String ringtoneUrl) {
        this.ringtoneUrl = ringtoneUrl;
    }

    public boolean isShowCallerId() {
        return showCallerId;
    }

    public void setShowCallerId(boolean showCallerId) {
        this.showCallerId = showCallerId;
    }
}
